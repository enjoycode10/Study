package com.okgo.leetcode.linkedlist.doublepoints.q19;

/**
 * @author Shawn
 * @date 2020/12/11 16:10
 * @title Function
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}